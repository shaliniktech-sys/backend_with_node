// ===========================================
// NPM Versioning
// ===========================================

// Semantic Versioning (SemVer)

// Format:
// MAJOR.MINOR.PATCH

// Example:
// 5.2.1

// ---------------------------
// PATCH
// ---------------------------

// Small bug fixes
// Performance improvements
// Minor security fixes

// Example:
// 5.2.1 -> 5.2.2

// ---------------------------
// MINOR
// ---------------------------

// New features
// Improvements
// Backward compatible updates

// Example:
// 5.2.1 -> 5.3.0

// ---------------------------
// MAJOR
// ---------------------------

// Breaking changes
// Existing projects may require code changes.

// Example:
// 5.2.1 -> 6.0.0

// ------------------------------------
// Install a Specific Version
// ------------------------------------

// npm install express@4.18.2

// ------------------------------------
// Caret (^)
// ------------------------------------

// ^5.2.1

// Allows:
// 5.2.2
// 5.3.0
// 5.8.4

// Doesn't allow:
// 6.0.0

// ------------------------------------
// Tilde (~)
// ------------------------------------

// ~5.2.1

// Allows:
// 5.2.2
// 5.2.8

// Doesn't allow:
// 5.3.0
// 6.0.0

// ------------------------------------
// Exact Version
// ------------------------------------

// 5.2.1

// Installs only:
// 5.2.1
